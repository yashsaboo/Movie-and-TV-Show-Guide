package insertData;

//Generates IDs based on the range given
public class GenerateID {
	
	public static void main(String[] args) {
		int start = 1;
		int stop = 5;
		for(int i=1; i<=stop; i++)
		{
			System.out.println(start);
			System.out.println(start);
			System.out.println(start++);
		}
		
	}

}
